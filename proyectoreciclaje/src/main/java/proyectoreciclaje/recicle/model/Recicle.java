package proyectoreciclaje.recicle.model;
//Aqui hemos creado la clase Recicle, donde almacenamos los valores del usuario que previamente usaremos
public class Recicle {
	private String recicle;
	private String date;
	private String location;
	
	
	public String getRecicle() {
		return recicle;
	}
	public void setRecicle(String recicle) {
		this.recicle = recicle;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	
	
}
