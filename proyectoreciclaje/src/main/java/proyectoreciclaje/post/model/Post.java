package proyectoreciclaje.post.model;
//Aqui hemos creado la clase Post, donde almacenamos los valores del usuario que previamente usaremos
public class Post {

	private String title;
	private String context;
	private String date;
	
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContext() {
		return context;
	}
	public void setContext(String context) {
		this.context = context;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
	
	
	
}
